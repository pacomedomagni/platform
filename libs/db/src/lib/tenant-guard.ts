import { AsyncLocalStorage } from 'async_hooks';
import { Logger } from '@nestjs/common';
import { Prisma } from '@prisma/client';

const logger = new Logger('TenantGuard');

/**
 * CLS-like store so `bypassTenantGuard(fn)` lets a legitimate cross-tenant
 * operation (provisioning, cron sweep, domain resolver) opt out of the guard
 * for the duration of `fn`. Uses Node's native async_hooks so it correctly
 * propagates through awaits, promises, and BullMQ worker callbacks.
 */
const bypassStorage = new AsyncLocalStorage<boolean>();

/**
 * Run `fn` with the tenant guard bypassed. Use sparingly and only for code
 * paths that genuinely cross tenant boundaries (tenant provisioning, cleanup
 * cron, domain resolver, authentication lookups by token).
 */
export function bypassTenantGuard<T>(fn: () => Promise<T>): Promise<T>;
export function bypassTenantGuard<T>(fn: () => T): T;
export function bypassTenantGuard<T>(fn: () => T | Promise<T>): T | Promise<T> {
  return bypassStorage.run(true, fn);
}

export function isTenantGuardBypassed(): boolean {
  return bypassStorage.getStore() === true;
}

/**
 * Storage for the tenantId override we accept when CLS is unavailable
 * (scheduled jobs, webhook handlers, queue workers). The Prisma RLS
 * extension reads from this BEFORE consulting nestjs-cls — letting a
 * caller temporarily impersonate a tenant without standing up a full
 * Nest CLS context is the cheapest way to make scheduled per-tenant
 * work behave like a normal request.
 */
const tenantOverride = new AsyncLocalStorage<string>();

/**
 * Run `fn` as if the request came from `tenantId`. Use this in scheduled
 * jobs / webhook handlers when iterating per-connection so the queries
 * inside hit the correct RLS policy.
 *
 *   for (const conn of connections) {
 *     await runWithTenant(conn.tenantId, () => syncOrdersForConnection(conn.id));
 *   }
 */
export function runWithTenant<T>(
  tenantId: string,
  fn: () => Promise<T>,
): Promise<T>;
export function runWithTenant<T>(tenantId: string, fn: () => T): T;
export function runWithTenant<T>(
  tenantId: string,
  fn: () => T | Promise<T>,
): T | Promise<T> {
  return tenantOverride.run(tenantId, fn);
}

export function getTenantOverride(): string | undefined {
  return tenantOverride.getStore();
}

/**
 * Assert that a write payload contains a tenantId. Call from service write paths
 * until the Prisma extension is wired in Phase 2.
 */
export function assertTenantWrite(model: string, data: unknown): void {
  if (isTenantGuardBypassed()) return;
  if (!TENANT_SCOPED_MODELS.has(model)) return;
  if (!dataHasTenantId(data)) {
    throw new TenantGuardError(
      `TenantGuard: write to ${model} without tenantId in data`,
    );
  }
}

/**
 * Assert that a where clause scopes by tenantId (directly or via AND/OR).
 */
export function assertTenantWhere(model: string, where: unknown): void {
  if (isTenantGuardBypassed()) return;
  if (!TENANT_SCOPED_MODELS.has(model)) return;
  if (!whereHasTenantId(where)) {
    throw new TenantGuardError(
      `TenantGuard: query on ${model} without tenantId filter`,
    );
  }
}

